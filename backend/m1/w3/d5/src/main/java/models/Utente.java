package models;

public class Utente {
	
	String nome;
	String cognome;
	int dataNascita;
	int numeroTessera;
	
	public Utente(String nome, String cognome, int dataNascita, int numeroTessera) {
		this.nome = nome;
		this.cognome = cognome;
		this.dataNascita = dataNascita;
		this.numeroTessera = numeroTessera;
	}
	
	

}
