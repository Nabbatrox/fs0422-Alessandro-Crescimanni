package esercizio3.classi;

public class SWITCH {
	
	public static void metodoSwitch(int x) {
			
			
			switch(x) {
			
			case 0:
				
				System.out.println("ZERO");	
				break;
			
			case 1:
				
				System.out.println("UNO");
				break;
				
			case 2:
				
				System.out.println("DUE");
				break;
				
			case 3:
				
				System.out.println("TRE");
				
				break;
			
			
			}
					
		
	}

}
